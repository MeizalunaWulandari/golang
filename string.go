package main

import "fmt"

func main(){
	fmt.Println("Luna")
	fmt.Println("Meizaluna")
	fmt.Println("Meizaluna Wulandari")
	fmt.Println(len("Meizaluna Wulandari"))
	fmt.Println("Meizaluna Wulandari"[0])
}
/*
	string harus pakai petik dua(")
	len untuk menghitung jumlah karakter
	[0] untuk mengambil karakter tentu dimulai dari 0 dan akan menghasilkan representasi byte dari string yang diambil

*/